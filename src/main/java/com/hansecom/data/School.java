package com.hansecom.data;

public class School {
    protected String name;
    protected int age;
    protected boolean isStudent;
    protected double avgScore;
    public String getName() {
        return name;
    }
    public int getAge(){
        return age;
    }
    public boolean getIsStudent(){
        return isStudent;
    }
    public double getAvgScore(){
        return avgScore;

    }

}
